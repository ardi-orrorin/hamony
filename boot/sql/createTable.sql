    `ID`	BIGINT UNSIGNED	NOT NULL	COMMENT 'PK',
    `REF_DIARY_ID`	BIGINT UNSIGNED	NOT NULL,
    `REF_TAG_ID`	BIGINT UNSIGNED	NOT NULL	COMMENT 'PK'
);

CREATE TABLE `TBL_URL` (
    `ID`	BIGINT UNSIGNED	NOT NULL,
    `URL`	TEXT	NOT NULL	COMMENT 'URL주소',
    `CREATEAT`	TIMESTAMP	NOT NULL	COMMENT '생성일자',
    `DELETEAT`	TIMESTAMP	NULL	COMMENT '삭제날짜',
    `AVAIL`	BOOLEAN	NOT NULL	COMMENT '사용여부',
    `REF_DIARY_ID`	BIGINT UNSIGNED	NOT NULL	COMMENT '다이어리'
);

CREATE TABLE `TBL_FILE` (
    `ID`	BIGINT UNSIGNED	NOT NULL,
