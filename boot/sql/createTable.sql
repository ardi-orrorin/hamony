    `REF_TAG_ID`	BIGINT UNSIGNED	NOT NULL	COMMENT '태그',
    `REF_MEMBER_ID`	BIGINT UNSIGNED	NOT NULL	COMMENT '유저'
);

CREATE TABLE `TBL_LIKE` (
