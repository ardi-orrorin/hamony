CREATE TABLE `TBL_DIARY` (
    `ID`	BIGINT UNSIGNED	NOT NULL,
    `SUBJECT`	VARCHAR(255)	NOT NULL	COMMENT '제목',
    `CONTENT`	TEXT	NOT NULL	COMMENT '내용',
    `CREATEAT`	TIMESTAMP	NOT NULL	COMMENT '생성날짜',
    `UPDATEAT`	TIMESTAMP	NULL	COMMENT '수정날짜',
    `DELETEAT`	TIMESTAMP	NULL	COMMENT '삭제날짜',
    `REF_MEMBER_ID`	BIGINT UNSIGNED	NOT NULL	COMMENT '유저'
);

CREATE TABLE `TBL_MEMBER` (
    `ID`	BIGINT UNSIGNED	NOT NULL	COMMENT 'PK',
    `USER_ID`	VARCHAR(255)	NOT NULL	COMMENT '계정아이디',
    `USER_PWD`	VARCHAR(255)	NOT NULL	COMMENT '계정패스워드',
    `EMAIL`	VARCHAR(255)	NOT NULL	COMMENT '이메일',
    `CREATEAT`	TIMESTAMP	NOT NULL	COMMENT '생성날짜',
    `DELETEAT`	TIMESTAMP	NULL	COMMENT '삭제날짜'
);

CREATE TABLE `TBL_TAG` (
    `ID`	BIGINT UNSIGNED	NOT NULL	COMMENT 'PK',
    `TAG`	VARCHAR(255)	NOT NULL	COMMENT '태그명'
);

